    public void setStockQty(String stockQty) {
        StockQty = stockQty;
    }

    public String getSalesQty() {
        return SalesQty;
    }

    public void setSalesQty(String salesQty) {
        SalesQty = salesQty;
    }

    public String getOrderQty() {
        return OrderQty;
    }

    public void setOrderQty(String orderQty) {
        OrderQty = orderQty;
    }
